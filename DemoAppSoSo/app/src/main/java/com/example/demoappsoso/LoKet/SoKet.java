package com.example.demoappsoso.LoKet;

public class SoKet {
    private String dong1;
    private String dong2;
    private String dong3;

    public SoKet(String dong1, String dong2, String dong3) {
        this.dong1 = dong1;
        this.dong2 = dong2;
        this.dong3 = dong3;
    }

    public String getDong1() {
        return dong1;
    }

    public void setDong1(String dong1) {
        this.dong1 = dong1;
    }

    public String getDong2() {
        return dong2;
    }

    public void setDong2(String dong2) {
        this.dong2 = dong2;
    }

    public String getDong3() {
        return dong3;
    }

    public void setDong3(String dong3) {
        this.dong3 = dong3;
    }
}
